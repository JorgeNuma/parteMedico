DROP TABLE USUARIOS IF EXISTS;
DROP TABLE REPORTES IF EXISTS;
DROP TABLE SECUENCIAS	IF EXISTS;


CREATE TABLE USUARIOS(
	
	DNI     			VARCHAR(9)    NOT NULL	,
	NOMBRE  			VARCHAR(100)	,
	APELLIDO1 			VARCHAR(100)	,
	APELLIDO2 			VARCHAR(100)	,
	SEXO 				VARCHAR(20)		,
	FECHA_NACIMIENTO 	DATE			,
	ALTURA 				INT 			,
	OBSERVACIONES 		VARCHAR(150)	,
	
	PRIMARY KEY(DNI)
	
);

CREATE TABLE REPORTES(
	
	CODIGO 				INT    NOT NULL	,
	DNI_USUARIO  		VARCHAR(9)    NOT NULL	,
	HORA_REPORTE  		TIMESTAMP			,
	LONGITUD 			VARCHAR(100)	,
	LATITUD 			VARCHAR(100)	,
	SISTOLICA		 	DOUBLE			,
	DIASTOLICA			DOUBLE			,
	PESO 				DOUBLE  		,
	NUMERO_PASOS 		INT 			,
	
	PRIMARY KEY(CODIGO)					,
	FOREIGN KEY(DNI_USUARIO) REFERENCES USUARIOS(DNI)
	
);

CREATE TABLE SECUENCIAS(

	NOMBRE_SECUENCIA	VARCHAR(100)	NOT NULL,
	VALOR_SECUENCIA		BIGINT			NOT NULL

); 